package com.qigan.qiganshop.exception;

/**
 * @author wyy
 *
 */
public class XltcTokenRuntimeException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	public XltcTokenRuntimeException(String message){
		super(message);
	}
	
	public XltcTokenRuntimeException(String message, Throwable throwable) {
		super(message, throwable);
	}

	public static XltcTokenRuntimeException throwable(String message) {
		return new XltcTokenRuntimeException(message);
	}

	public static XltcTokenRuntimeException throwable(String message, Throwable throwable) {
		return new XltcTokenRuntimeException(message, throwable);
	}

}
