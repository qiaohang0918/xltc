package com.qigan.qiganshop.pojo;

import java.util.List;

/**
 * @author wyy
 *
 */
public class XltcHomeModel {
	
	private List<Banner> activities;
	
	private List<Banner> banners;
	
	private List<Banner> ads;
	
	private XltcHomeGoodsModel goodsData;
	
	private XltcPageResult userAddrs;

	public List<Banner> getActivities() {
		return activities;
	}

	public void setActivities(List<Banner> activities) {
		this.activities = activities;
	}

	public List<Banner> getBanners() {
		return banners;
	}

	public void setBanners(List<Banner> banners) {
		this.banners = banners;
	}

	public XltcPageResult getUserAddrs() {
		return userAddrs;
	}

	public void setUserAddrs(XltcPageResult userAddrs) {
		this.userAddrs = userAddrs;
	}

	public List<Banner> getAds() {
		return ads;
	}

	public void setAds(List<Banner> ads) {
		this.ads = ads;
	}
	
	public XltcHomeGoodsModel getGoodsData() {
		return goodsData;
	}

	public void setGoodsData(XltcHomeGoodsModel goodsData) {
		this.goodsData = goodsData;
	}

	@Override
	public String toString() {
		return "XltcHomeModel [activities=" + activities + ", banners=" + banners + ", ads=" + ads + ", goodsData="
				+ goodsData + ", userAddrs=" + userAddrs + "]";
	}

}
