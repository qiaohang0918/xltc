package com.qigan.qiganshop.enums;

/**
 * @author wyy
 *
 */
public enum DictType {
	
	CancelDelived("取消配送", "cancelDelived"),
	
	RefuseDelived("拒收", "refuseDelived");
	
	private String text;
	
	private String value;
	
	private DictType(String text, String value){
		this.text = text;
		this.value = value;
	}
	
	public String getText(){
		return text;
	}
	
	public String getValue(){
		return value;
	}
}
